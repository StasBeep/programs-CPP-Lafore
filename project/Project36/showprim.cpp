// showprim.cpp
// изображает распределение простых чисел

#include <iostream>
using namespace std;
#include <conio.h>			// дл€ _getche()

int main()
{
	const unsigned char WHITE = 219;	// белый цвет дл€ простых чисел
	const unsigned char GRAY = 176;		// серый цвет дл€ остальных чисел
	unsigned char ch;
	// действие дл€ каждой позици и экрана
	for (int count = 0; count < 80 * 25 - 1; count++)
	{
		ch = WHITE;				// предпологаем, что число простое
		for (int j = 2; j < count; j++)		// делим на каждое целое, больше 2
			if (count % j == 0)				// если остаток равен 0
			{
				ch = GRAY;					// то число не простое
				break;						// выход из внктреннего цикла
			}
		cout << ch;						// вывод символа на экран
	}
	_getch();							// задержка полученного изображени€
	return 0;
}