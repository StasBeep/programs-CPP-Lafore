#include <iostream>
using namespace std;

///////////// class Distance /////////////////////////////////////////
class Distance			// длина в английской системе
{
private:
	int feet;
	float inches;
public:
	void setdist(int ft, float in)	// установка значений полей
	{
		feet = ft;
		inches = in;
	}
	void getdist()			// ввод полей с клавиатуры
	{
		cout << "\n ¬ведите число футов: ";
		cin >> feet;
		cout << "¬ведите число дюймов: ";
		cin >> inches;
	}
	void showdist()		// вывод полей на экран
	{
		cout << feet << "\'-" << inches << "\"";
	}
};

///////////////// int main() /////////////////////////////////////////
int main()
{
	setlocale(LC_ALL, "Russian");
	Distance dist1, dist2;		// две длины
	dist1.setdist(11, 6.25);	// установка значений дл€ d1
	dist2.getdist();			// ввод значений дл€ dist2
	// вывод длин на экран
	cout << "\ndist1 = ";
	dist1.showdist();
	cout << "\ndist2 = ";
	dist2.showdist();
	cout << endl;
	return 0;
}